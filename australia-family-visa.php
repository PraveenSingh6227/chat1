		  <li>You need to be engaged to a permanent resident of Australia, a citizen of that country, or a citizen of New Zealand who qualifies.</li>
		  <li>Your intended spouse or partner must sponsor your visa application.</li>
		  <li>It must be your intention to wed within the next nine months.</li>
		  <li>Fulfill the requisite standards for character and health.</li>
		  <li>Must pay off all of your outstanding debts to the Australian government, if any</li>
          </ul>
