<p><strong>Orphan Relative Visa (Subclass 117/Subclass 837):</strong> This type of visa permits an international orphan child (under the age of eighteen) to reside in Australia with family members. In order to be eligible to apply for this visa, the child's parents must be deceased, disabled, or missing.</p>

<p><strong>Dependent Child Visa (Subclass 445):</strong> This stream applies to children whose parents are in the process of applying for permanent residency but currently have a temporary partner visa.</p>
