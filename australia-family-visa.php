          <p><strong>Parent Visa (Subclass 103)</strong></p>
          <p>The qualifying parents of Australian citizens or permanent residents can reunite with their children in Australia thanks to this permanent residence visa. Your child who has established in Australia will sponsor you under the terms of the parent visa.<br />
            &nbsp;</p>
          <p>There are three additional categories for the parent visa to Australia:<br />
            &nbsp;</p>
          <p><strong>Parent Category- </strong>Parents or other family members may be sponsored by Australian citizens or permanent residents to join them in Australia for a temporary or permanent stay under this visa category.</p>
		  
          <p><strong>Contributory Parent- </strong>There are several visa tracks or migration schemes that are part of the contributing parent visa. You can remain in Australia as a permanent resident by submitting an application for this visa. However, you must pass the balance-of-family test—that is, have half of your children living as legal residents or citizens of Australia—in order to be eligible for this visa.</p>
          <p>A letter of support is also required if you want a permanent visa under the previously indicated category. Those who apply for the Retirement Pathway are not required to do the same.</p>
          <p><strong>Sponsored Parent</strong>- In order to assist parents or grandparents in joining their children in Australia, a temporary visa category was introduced in March 2019. A parent may reside in Australia for up to five years under the visa pathway; however, the parent must leave the country for at least ninety days prior to reapplying for the visa.</p>
          <p><strong>Aged Parent Visa-</strong> Older parents who want to go to Australia with their children who are already citizens or residents of Australia are awarded the visa. Prior to applying for this visa, the prospective parents must have their children's sponsorship.</p>
