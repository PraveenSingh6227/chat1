          <p>Those who wish to marry a permanent resident or citizen of Australia or a citizen of New Zealand who meets the requirements can apply for an Australian prospective marriage visa. The visa is only good for nine months, during which time you have to tie the knot with your intended spouse. During these nine months, Subclass 300 holders are allowed to apply for full-time jobs or pursue independent studies at their own expense. After being married, you can apply for a partner visa, which would eventually grant you <strong>permanent residency in Australia</strong>.
</p>
