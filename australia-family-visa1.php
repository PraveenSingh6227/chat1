          <p>Also known as Subclass 801, the Partner Visa for Australia allows the spouse or de facto partner of an Australian citizen or permanent resident to migrate to Australia. The visa is applicable for married partners, same sex partners, and or a fianc&eacute;e/fianc&eacute; of an Australian citizen, or an eligible New Zealand citizen.</p>
          <p>Your application for Partner Visa to Australia must be lodged or sponsored by your partner already living in Australia for a period of two years.</p>
