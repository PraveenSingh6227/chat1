            <li>You must be engaged to an Australian permanent resident, or a citizen, or an eligible citizen of New Zealand.</li>
            <li>Your visa application must be sponsored by your prospective spouse/partner.</li>
            <li>You must be intended to marry each other within 9-months&rsquo; period</li>
            <li>Meet necessary health and character requirements</li>
            <li>Must clear all your debts to the Australia government, in case you have any. &nbsp;</li>
          </ul>
