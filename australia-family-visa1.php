          <p><strong>Parent Visa (Subclass 103)</strong></p>
          <p>It&rsquo;s a permanent resident visa that permits the eligible parents of Australian citizens or permanent residents to reunite with their children in Australia. Under Australia parent visa, you&rsquo;ll be sponsored by your child settled in the country.<br />
            &nbsp;</p>
          <p>The parent visa for Australia can be further divided into three categories:<br />
            &nbsp;</p>
