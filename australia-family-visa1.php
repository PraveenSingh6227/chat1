          <p><strong>Orphan Relative Visa (Subclass 117/ Subclass 837)- </strong>The Orphan Relative is a visa category that allows an Orphan child overseas (below 18 years) to live with his/her relatives in Australia. To apply for this visa, the parents of the child must be dead, incapacitated or unable to found.</p>
          <p><strong>Dependent Child Visa (Subclass 445)- </strong>The stream is applicable for children whose parents are the holders of temporary partner visa, and in the process of obtaining the permanent visa in Australia.</p>
