          <p><strong>Basic Requirements for Partner Visa Australia</strong></p>
          <p>You are eligible to apply for the partner visa Australia if you meet the following requirements:</p>
          <ul>
            <li>Both partners must be 18 years of age or above;</li>
            <li>The applicant must be spouse/fianc&eacute;/ de facto partner of the person residing in Australia.&nbsp;</li>
            <li>Your spouse must be able to sponsor you for at least two years.</li>
