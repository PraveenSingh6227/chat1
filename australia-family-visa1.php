          <p><strong>Contributory Parent- </strong>The contributory parent visa comprises a number of visa pathways or migration programs. By applying for this visa, you may stay in Australia as a permanent resident. However, in order to qualify for this visa, you must meet the balance-of-family test, which means half of your children must be living as lawful Australian residents or citizens.</p>
