            <li>Parents who are applying as a retiree do not need any sponsor for this visa</li>
            <li>Meet the balance-of-family test, which means half of your children must be lawfully living in Australia.</li>
            <li>Be able to obtain an assurance of support</li>
            <li>Meet the health and character requirements</li>
            <li>Have cleared all your debts with the government of Australia, in case you have</li>
