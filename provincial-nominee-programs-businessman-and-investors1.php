<li>Your registration is legitimate for a period of 6 months from the time you obtain your total registration score. In case you fail to get an invite to submit an application prior to your registration concludes the same will no longer be legitimate only to be removed from the selection pool. You may afterwards generate and present a fresh registration.</li>
<li>You can just have one active registration at a given time.</li>
