          <p><strong>Relocate to Australia through the New Subclass 494 Skilled Employer Sponsored Regional Visa!</strong></p>
          <p>On 16<sup>th</sup> November 2019, the Subclass 494 Skilled Employer Sponsored Regional Visa was introduced to replace the Subclass 187 Regional Sponsored Migration Scheme Visa.</p>
          <p>The Australian government intends to direct more foreign talent towards the country&rsquo;s regional areas, which are facing major workforce shortages in several key occupations. To increase immigrant inflow and retention in these areas, the Department of Home Affairs (DHA) has launched two innovative new visas &ndash; the <strong><a href="skilled-work-regional-provisional-visa-491.php" target="_blank">Subclass 491 Visa</a></strong> and the Subclass 494 Visa.</p>
          <p><br />
            <strong>What is the Skilled Employer Sponsored Regional Visa?</strong></p>
          <p>This <a href="javascript:avoid(0)"><strong>Australia visa</strong></a> allows employers in regional Australia to sponsor overseas skilled workers who can fulfill local labor market requirements. Through this pathway, foreign nationals &ndash; who are qualified to occupy job vacancies that cannot be filled by the domestic Australian population &ndash; can live, work and study in a designated regional area for up to five years.</p>
          <p><br />
            <strong>Can an Australian employer nominate a foreign skilled worker for this visa?</strong></p>
          <p>The Subclass 494 Visa has two sub-categories &ndash; the Employer Sponsored stream and the Labor Agreement stream. Under the Employer Sponsor stream, an eligible Australian employer can nominate a foreign worker to work in an occupation that is facing skills shortages in the local labor market &ndash; in a position under the sponsoring business or an associated entity of such business, located in a designated regional area. This occupation is known as the nominated skilled occupation.</p>
          <p>The employer must be lawfully operating a business in Australia, and be an approved work sponsor or have applied to become a standard business sponsor, in order to submit a nomination application for the foreign worker.</p>
          <p><br />
            <strong>How will the introduction of this visa impact the validity of the Subclass 187 Visa?</strong></p>
          <p>Since 16<sup>th</sup> November 2019, the Subclass 187 Visa has been closed to new applications.</p>
          <p><br />
            <strong>What will happen to Subclass 187 Visa applications that are awaiting a decision from the DHA?</strong></p>
          <p>Pending RSMS Visa applications submitted prior to 16<sup>th</sup> November 2019 will continue to be processed as per the transitional arrangements. Subsequently, the DHA will declare the visa outcome.</p>
